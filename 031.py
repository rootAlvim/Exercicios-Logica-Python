num = int(input("Digite um número de 1 a 7: "))
if num == 1:
    print("O dia da semana correspondente ao número e domingo!") 
elif num == 2:
    print("O dia da semana correspondente ao número e segunda-feira!")
elif num == 3:
    print("O dia da semana correspondente ao número e terça-feira!")
elif num == 4:
    print("O dia da semana correspondente ao número e quarta-feira!")
elif num == 5:
    print("O dia da semana correspondente ao número e quinta-feira!")
elif num == 6:
    print("O dia da semana correspondente ao número e sexta-feira!")
elif num == 7:
    print("O dia da semana correspondente ao número e sábado!")
else:
    print("Número fora da faixa permitida!")